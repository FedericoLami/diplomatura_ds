cadena = "Geringoso"
capadepenapa = ""

for c in cadena:
    capadepenapa+=c
    if c=="a":
        capadepenapa+="pa"
    if c=="e":
        capadepenapa+="pe"
    if c=="i":
        capadepenapa+="pi"
    if c=="u":
        capadepenapa+="pu"
    if c=="o":
        capadepenapa+="po"
        
print (capadepenapa)
